function quadrado(numero) {
  return numero * numero;
}

let resultado1 = quadrado(2);
console.log(resultado1); // Saída: 4

let resultado2 = quadrado(5);
console.log(resultado2); // Saída: 25

let resultado3 = quadrado(10);
console.log(resultado3); // Saída: 100

let resultado4 = quadrado(0.5);
console.log(resultado4); // Saída: 0.25

