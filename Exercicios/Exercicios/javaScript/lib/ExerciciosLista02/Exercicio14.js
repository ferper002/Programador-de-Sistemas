function perimetroCirculo(raio) {
    const pi = 3.14159;
    return 2 * pi * raio;
  }
  let resultado1 = perimetroCirculo(7);
  console.log(resultado1); // Saída: 43.9823
  
  let resultado2 = perimetroCirculo(10);
  console.log(resultado2); // Saída: 62.8318
  
  let resultado3 = perimetroCirculo(3.5);
  console.log(resultado3); // Saída: 21.9912